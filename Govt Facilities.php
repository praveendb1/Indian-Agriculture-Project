    <h2 class="text-success py-3">Government schemes in agriculture</h2>
       

    <a href="https://soilhealth.dac.gov.in/"><h4 class="text-info py-3">1. Soil Health Card Scheme</h4></a>
        <p class=font-weight-bold>
            The Soil Health Cards provide information to farmers on nutrient status of their soil along
            with recommendation on appropriate dosage of nutrients to be applied for improving soil
            health and its fertility.
        </p>
<a href="https://pmfby.gov.in/"><h4 class="text-info py-3">2. Pradhan Mantri Fasal Bima Yojana (PMFBY)</h4></a>
<p class=font-weight-bold>
PMFBY is an actuarial premium based scheme under which farmer has to pay maximum premium
 of 2% for Kharif, 1.5% for Rabi food & oilseed crops and 5% for annual commercial/
 horticultural crops and remaining part of the actuarial/bidded premium is shared equally
  by the Centre and State Government.  One of the objectives of the scheme is to facilitate
   prompt claims settlement. The claims must be settled within two months of harvest subject 
   to timely provision of both yield data and share of premium subsidy by the State Government.

</p>
<a href="https://www.nationalfertilizers.com/index.php?option=com_content&view=article&id=139&Itemid=158&lang=en">
<h4 class="text-info py-3">3. Neem Coated Urea (NCU)</h4></a>
<p class=font-weight-bold>
This scheme is initiated to regulate use of urea, enhance availability of nitrogen to the crop
 and reduce cost of fertilizer application.  NCU slows down the release of fertilizer and makes
  it available to the crop in an effective manner. The entire quantity of domestically
   manufactured and imported urea is now neem coated.  It reduces the cost of cultivation 
   and improves soil health management. 

</p>
<a href="https://pmksy.gov.in/">
<h4 class="text-info py-3">4. Pradhan Mantri Krishi Sinchai Yojana (PMKSY)</h4></a>
<p class=font-weight-bold>
It was launched on 1st July, 2015 with the motto of ‘Har Khet Ko Paani’ for providing end-to
 end solutions in irrigation supply chain, viz. water sources, distribution network and farm 
 level applications.<br> 
PMKSY not only focuses on creating sources for assured irrigation, but also creating protective
 irrigation by harnessing rain water at micro level through ‘Jal Sanchay’ and ‘Jal Sinchan’.  <br>
Micro irrigation is to be popularised to ensure ‘Per drop-More crop’. PMKSY adopts State 
level planning and projectised execution that allows States to draw up their own irrigation 
development based on District Irrigation Plans and State Irrigation Plans. 
</p>
<h5>Components: </h5>
<ul class="font-weight-bold">
<li>Accelerated Irrigation Benefit Programme(AIBP): implemented by Ministry of Water Resources,
 RD & GR.</li>
 <li>
 PMKSY (Har Khet ko Pani): implemented by Ministry of Water Resources, RD & GR

 </li>
 <li>
 PMKSY (Watershed): implemented by Department of Land Resources.
 </li>
 <li>
 PMKSY(Per Drop More Crop - PDMC)
 </li>
</ul>
<a href="https://pgsindia-ncof.gov.in/pkvy/index.aspx">
<h4 class="text-info py-3">5. Paramparagat Krishi Vikas Yojana (PKVY)</h4></a>
<p class="font-weight-bold">
It is implemented with a view to promote organic farming in the country. To improve soil
 health and organic matter content and increase net income of the farmer so as to realise
  premium prices.  Under this scheme, an area of 5 lakh acre is targeted to be covered 
  though 10,000 clusters of 50 acre each, from the year 2015-16 to 2017-18. 

</p>
<a href="https://enam.gov.in/web/">
<h4 class="text-info py-3">6. National Agriculture Market (e-NAM)</h4></a>
<p class="font-weight-bold">
It provides e-marketing platform at national level and support creation of infrastructure to enable e-marketing. <br>
This innovative market process is revolutionizing agriculture markets by ensuring better price discovery. It brings in transparency and competition to enable farmers to get improved remuneration for their produce moving towards ‘One Nation One Market’.  

</p>
<a href="https://pib.gov.in/Pressreleaseshare.aspx?PRID=1532269">
<h4 class="text-info py-3">7. Micro Irrigation Fund (MIF)</h4></a>
<p class="font-weight-bold">
A dedicated MIF created with NABARD has been approved with an initial corpus of Rs. 5000 crore (Rs. 2000 crore for 2018-19 & Rs. 3000 crore for 2019-20) for encouraging public and private investments in Micro irrigation. The main objective of the fund is to facilitate the States in mobilizing the resources for expanding coverage of Micro Irrigation.<br> 
MIF would not only facilitate States in incentivizing and mobilizing resources for achieving the target envisaged under PMKSY-PDMC but also in bringing additional coverage through special and innovative initiatives by State Governments. <br>

An Advisory Committee has been set up to provide policy direction and ensure effective planning, coordination and monitoring of the Micro Irrigation Fund. <br> 

</p>
<a href="http://agricoop.nic.in/agriculture-contingency-plan-listing">
<h4 class="text-info py-3">8. Agriculture Contingency Plan </h4></a>
<p class="font-weight-bold">
Central Research Institute for Dryland Agriculture (CRIDA), ICAR has prepared district level Agriculture Contingency Plans in collaboration with state agricultural universities using a standard template to tackle aberrant monsoon situations leading to drought and floods, extreme events (heat waves, cold waves, frost, hailstorms, cyclone) adversely affecting crops, livestock and fisheries (including horticulture).<br>  
Total 614 district agriculture contingency plans are placed in the ‘farmer portal’ of the Ministry of Agriculture and Farmers Welfare, Government of India and also in the ICAR / CRIDA 

</p>
<a href="https://www.indiafilings.com/learn/rainfed-area-development-programme/">
<h4 class="text-info py-3">9. Rainfed Area Development Programme (RADP)</h4></a>
<p class="font-weight-bold">
Rainfed Area Development Programme (RADP) was implemented as a sub-scheme under Rashtriya Krishi Vikas Yojana (RKVY).
<h5>Aim: </h5>
<ul class="font-weight-bold text-justify">
<li>
To improve quality of life of farmers’ especially, small and marginal farmers by offering a complete package of activities to maximize farm returns.
</li>
<li>
Increasing agricultural productivity of rainfed areas in a sustainable manner by adopting appropriate farming system based approaches.
</li>

<li>
To minimise the adverse impact of possible crop failure due to drought, flood or un-even rainfall distribution through diversified and composite farming system.
</li>
<li>
Restoration of confidence in rainfed agriculture by creating sustained employment opportunities through improved on-farm technologies and cultivation practices
</li>
<li>
Enhancement of farmer’s income and livelihood support for reduction of poverty in rainfed areas
</li>
</ul>
</p>
<a href="https://www.indiastat.com/social-and-welfare-schemes-data/27/rural-schemes/247/national-watershed-development-projects-in-rainfed-areas-nwdpra-1997-2019/380377/stats.aspx">
<h4 class="text-info py-3">10. National Watershed Development Project for Rainfed Areas (NWDPRA)</h4></a>
<p class="font-weight-bold">
The scheme of National Watershed Development Project for Rainfed Areas (NWDPRA) was launched in 1990-91 based on twin concepts of integrated watershed management and sustainable farming systems. 
</p>
<a href="https://nmsa.dac.gov.in/">
<h4 class="text-info py-3">11. National Mission for Sustainable Agriculture (NMSA)</h4></a>
<p class="font-weight-bold">
NMSA is one of the eight Missions under National Action Plan on Climate Change (NAPCC). It aims at promoting Sustainable Agriculture through climate change adaptation measures, enhancing agriculture productivity especially in rainfed areas focusing on integrated farming, soil health management, and synergizing resource conservation. <br>
NMSA as a programmatic intervention caters to Mission Deliverables that focuses mainly on conservation agriculture to make farm sector more productive, sustainable, remunerative and climate resilient by promoting location specific integrated/composite farming systems. <br>

</p>

